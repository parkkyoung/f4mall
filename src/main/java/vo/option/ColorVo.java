package vo.option;

public class ColorVo {
	int color_no;
	String color_name;
	
	
	public int getColor_no() {
		return color_no;
	}
	public void setColor_no(int color_no) {
		this.color_no = color_no;
	}
	public String getColor_name() {
		return color_name;
	}
	public void setColor_name(String color_name) {
		this.color_name = color_name;
	}
}
